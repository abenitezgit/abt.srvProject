package eco.model;

public class QueryDataEnt {
	String ent; //entidad
	String exs; //existe
	String atr; //accion_transferencia
	String maa;	//mensaje_accion_audio
	
	public String getEnt() {
		return ent;
	}
	public void setEnt(String ent) {
		this.ent = ent;
	}
	public String getExs() {
		return exs;
	}
	public void setExs(String exs) {
		this.exs = exs;
	}
	public String getAtr() {
		return atr;
	}
	public void setAtr(String atr) {
		this.atr = atr;
	}
	public String getMaa() {
		return maa;
	}
	public void setMaa(String maa) {
		this.maa = maa;
	}
}
